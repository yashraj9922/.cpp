class Rectangle
{
private:
    int length;
    int breadth;

public:
    // using getters and setters
    void setLength(int l)
    {
        length = l;
    }
    void setBreadth(int b)
    {
        breadth = b;
    }
    int
    getLength()
    {
        return length;
    }
    int getBreadth()
    {
        return breadth;
    }
};